const MetricCard: React.FC<{stat: HeadlineStat; delay: number}> = ({stat, delay}) => {
	const frame = useCurrentFrame();
	const {fps} = useVideoConfig();
	const pop = spring({frame: frame - delay, fps, config: {damping: 12, stiffness: 130}});
	return (
		<div
			style={{
				...styles.metricCard,
				borderColor: colors[stat.tone],
				transform: `translateY(${(1 - pop) * 24}px) scale(${0.92 + pop * 0.08})`,
				opacity: pop,
			}}
		>
			<div style={{...styles.metricGlow, background: colors[stat.tone]}} />
			<div style={styles.metricValue}>{stat.value}</div>
			<div style={styles.metricLabel}>{stat.label}</div>
		</div>
	);
};

