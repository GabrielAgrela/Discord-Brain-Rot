const useSceneProgress = (start: number, duration: number) => {
	const frame = useCurrentFrame();
	const {fps} = useVideoConfig();
	const localFrame = frame - start;
	const entrance = spring({
		frame: localFrame,
		fps,
		config: {
			damping: 16,
			stiffness: 110,
			mass: 0.9,
		},
	});
	const exit = interpolate(localFrame, [duration - 24, duration], [1, 0], {
		extrapolateLeft: 'clamp',
		extrapolateRight: 'clamp',
	});
	const active = localFrame >= 0 && localFrame < duration;
	return {
		active,
		opacity: active ? entrance * exit : 0,
		y: interpolate(entrance, [0, 1], [30, 0]),
		localFrame,
	};
};

